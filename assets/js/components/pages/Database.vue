    export default {
        name: "Database",
        data: function(){
            return{
                plants: null,
                category: '',
                search: '',
                difficulty: '',
                buttonPressed: false,
                findRandomPlant: false,
                loggedInUser:{},
                randomPlant: null
            }
        },
        created: function() {
            this.loggedInUser = JSON.parse(localStorage.getItem('user'));

            //Retrieve user item from local storage in case of login
            this.$root.$on('loggedIn', () => {
                this.loggedInUser = JSON.parse(localStorage.getItem('user'));
            });
            if(this.loggedInUser == null){
                this.$router.push('/login');
            }
        },
        mounted: function() {
            this.$http.get('/api/database/')
                .then(response => {
                    this.plants = response.data;
                })
                .catch(error => {
                    alert(error);
                });
        },

        computed: {

            filteredPlants: function () {
                return this.plants.filter((plant) => {
                    return (plant.name.toLowerCase().match(this.search.toLowerCase())
                        || plant.genus.toLowerCase().match(this.search.toLowerCase())
                        || plant.latinName.toLowerCase().match(this.search.toLowerCase()))
                        //|| plant.alternativeName.toLowerCase().match(this.search.toLowerCase()) can be null
                        && plant.careLevel.match(this.difficulty)
                        && plant.categorySelect.match(this.category)
                });

            }

        },

        methods: {
            checkRole: function () {
                let role = this.loggedInUser.roles;

                for(let i = 0; i < this.loggedInUser.roles.length; i++) {
                    console.log(this.loggedInUser.roles);
                    if ((role[i] !=null)) {
                        return true;
                    }
                }
            },
            chooseRandom: function () {
                this.randomPlant = this.plants[Math.round(Math.random() * (this.plants.length))];
            }
        }}


